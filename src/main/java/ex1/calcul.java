package ex1;

/**
 * Permet d'effectuer des calculs
 */
public class calcul {

	/**Permet d'effectuer une addition
	 * @param nb1 premier nombre à additioner
	 * @param nb2 deuxième nombre à additionner
	 * @return résultat de l'addition
	 */
	public int addition(int nb1, int nb2){
		return nb1+nb2;
	}
	
	/**Permet d'effectuer une soustraction
	 * @param nb1 premier nombre à soustraire
	 * @param nb2 deuxième nombre à soustraire
	 * @return résultat de la soustraction
	 */
	public int soustraction(int nb1, int nb2){
		return nb1+nb2;
	}
}
